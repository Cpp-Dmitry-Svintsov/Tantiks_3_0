#include "settings.h"

//=====================================================================================================================
// static const and variables
//=====================================================================================================================
float AsSettings::Fps = 40.f;
float *AsSettings::Delta_Time = nullptr;

//=====================================================================================================================
// constr & destr
//=====================================================================================================================
AsSettings::~AsSettings()
{}
AsSettings::AsSettings()
{}
//=====================================================================================================================
// public static methods
//=====================================================================================================================
void AsSettings::Init(float* dt)
{
	if (Delta_Time == nullptr)
		Delta_Time = dt;
}
//=====================================================================================================================
float AsSettings::Distance_Beetween_Two_Points(sf::Vector2f a, sf::Vector2f b)
{
	sf::Vector2f c = (b - a);
	float res = sqrtf(pow(c.x, 2) + pow(c.y, 2));
	return res;
}
//=====================================================================================================================
float AsSettings::Get_Delta_Time()
{
	//reeturn delta time * multiplier
	return *(Delta_Time) * Fps;
}
//=====================================================================================================================
float AsSettings::Get_Fps()
{
	if (Delta_Time != nullptr)
		return 1.f / *(Delta_Time);
	else
		return 0.f;
}
