//========================================================================================================================
ASpauner::~ASpauner()
{

}

ASpauner::ASpauner(int x, int y)
{
	Ceil_Pos.x = (float)x;
	Ceil_Pos.y = (float)y;
	Ceil_X = x;
	Ceil_Y = y;
}

//========================================================================================================================
// public sections:
//========================================================================================================================
sf::Vector2f ASpauner::Get_Pos()
{
	sf::Vector2f original(25.f, 25.f);
	return Ceil_Pos + original;
}
//========================================================================================================================
void ASpauner::Get_Pos(int &x, int &y)
{
	x = Ceil_X;
	y = Ceil_Y;
}
